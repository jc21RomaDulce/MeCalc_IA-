function sumar(a, b) {
    return a + b;
}

function restar(a, b) {
    return a - b;
}

function multiplicar(a, b) {
    return a * b;
}

function dividir(a, b) {
    return a / b;
}

function random() {
    return Math.floor(Math.random() * 100);
}

export { sumar, restar, multiplicar, dividir, random };